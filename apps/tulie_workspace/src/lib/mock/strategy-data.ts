export function formatCurrency(amount: number): string {
  if (amount >= 1000000) {
    return `${(amount / 1000000).toFixed(1)}M`;
  }
  if (amount >= 1000) {
    return `${(amount / 1000).toFixed(0)}K`;
  }
  return amount.toString();
}

export function getDealsByStage(deals: Deal[]) {
  const stages: Record<string, Deal[]> = {};
  for (const deal of deals) {
    if (!stages[deal.stage]) stages[deal.stage] = [];
    stages[deal.stage].push(deal);
  }
  return stages;
}

export function getTotalPipelineValue(deals: Deal[]): number {
  return deals.reduce(
    (sum, deal) => sum + deal.value * (deal.probability / 100),
    0,
  );
}

export function getWeightedValue(deals: Deal[]): number {
  return deals.reduce(
    (sum, deal) => sum + deal.value * (deal.probability / 100),
    0,
  );
}
